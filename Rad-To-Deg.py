#Напишете програма, която чете ъгъл в радиани (rad) и го преобразува в градуси (deg). Потърсете в Интернет
#подходяща формула. Числото π в Python програми е достъпно чрез math.pi. Закръглете резултата до най-
#близкото цяло число използвайки round().
import math
rad = float(input())
deg = rad * 180/math.pi
result = deg.__round__()

print(result)